/*
 * Behavioral Design pattern:-
 * Behavioral design patterns are design patterns that identify common communication patterns between objects and realize these patterns.
 * By doing so, these patterns increase flexibility in carrying out this communication.
 *
 * Observer:-
 * The observer pattern is a software design pattern in which an object, called the subject, maintains a list of its dependents,
 * called observers, and notifies them automatically of any state changes, usually by calling one of their methods.
 *
 * Strategy:-
 * The strategy pattern (also known as the policy pattern) is a software design pattern that enables an algorithm's behavior to be a selected at runtime.
 * The strategy pattern defines a family of algorithms, encapsulates each algorithm, and makes the algorithms interchangeable within that family.
 */
