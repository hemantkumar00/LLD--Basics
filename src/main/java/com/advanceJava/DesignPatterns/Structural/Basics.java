/*
 * Structural patterns:-
 * Structural patterns are design patterns that ease the design by identifying a simple way to realize relationships between entities.
 * Structural patterns are concerned with low classes and objects are composed to form larger structures.
 *
 * Adapter:-
 * The adapter pattern is a software design pattern (also known as wrapper, an alternative naming shared with the decorator pattern)
 * that allows the interface of an existing class to be used from another interface.
 * It is often used to make existing classes work with others without modifying their source code.
 *
 * Flyweight:-
 * Flyweight is a structural design pattern that lets you fit more objects into the available amount of
 * RAM by sharing common parts of state between multiple objects instead of keeping all the data in each object.
 *
 * Adapter:- pattern is a structural pattern that allows objects with incompatible interfaces to collaborate.
 *
 * Facade:- A facade is an object that provides a simplified interface to a larger body of code, such as a class library.
 *
 * Decorator:-
 * A structural design pattern that allows adding new behaviors to objects by placing
 * these objects inside special wrapper objects that contain the behaviors.
 *
 * Flyweight:-
 * The flyweight pattern is used to reduce the memory footprint of a program by sharing as much data as possible with similar objects
 *
 */
