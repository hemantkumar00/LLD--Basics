/*
 * UML is a general-purpose, developmental, modeling language in the field of software engineering that is intended to provide a standard way to visualize the design of a system.
 *
 * Structural diagrams:- These diagrams are used to describe the structure of a system.
 * They are used to describe the classes and objects in a system.
 * They are also a great way to describe the relationships between classes and objects.
 *
 * -> There are four main types of structural diagrams -
 * * Class diagram - This diagram is used to describe the classes and objects in a system.
 * It is also used to describe the relationships between classes and objects.
 * * Object diagram - This diagram is used to describe the objects in a system.
 * It is also used to describe the relationships between objects.
 * * Component diagram - This diagram is used to describe the components in a system.
 * It is also used to describe the relationships between components.
 * * Package diagram - This diagram is used to describe the packages in a system.
 * It is also used to describe the relationships between packages.
 *
 * Behavioral diagrams:- These diagrams are used to describe the behavior of a system.
 * They are used to describe the interactions between classes and objects.
 * They are also used to describe the sequence of events in a system.
 *
 * -> There are four main types of behavioral diagrams -
 * * Use case diagram - This diagram is used to describe the use case in a system and actors that interact with the system.
 * * Sequence diagram - This diagram is used to describe the sequence of events in a system.
 * * Activity diagram - This diagram is used to describe the flow of control in a system.
 * * State diagram - This diagram is used to describe the state of an object in a system.
 */
